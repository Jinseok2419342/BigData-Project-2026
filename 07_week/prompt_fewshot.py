import ollama

# 리뷰 → 카테고리 분류를 Few-shot으로
fewshot_prompt = """당신은 카페 리뷰를 분석하는 전문가입니다.
아래 예시를 참고하여 리뷰를 적절한 카테고리로 분류해주세요.

리뷰: "라떼가 진하고 맛있었어요"
카테고리: 맛

리뷰: "카페가 너무 시끄러워서 집중이 안 됐어요"
카테고리: 분위기

리뷰: "직원분이 친절하고 빠르게 서빙해 주셨어요"
카테고리: 서비스

리뷰: "케이크 양이 너무 적었어요"
카테고리: 양

리뷰: "커피가 식어서 나왔어요"
카테고리: 온도

이제 다음 리뷰를 보고, **카테고리 하나만** 정확하게 답변해주세요.
답변은 "카테고리: XXX" 형식으로만 작성하고, 다른 설명은 하지 마세요.

리뷰: "직원이 메뉴 추천을 자세히 해줘서 고르기 쉬웠어요"
카테고리:"""

response = ollama.generate(model = "gemma3:12b", prompt = fewshot_prompt)
print("분류 결과:", response["response"].strip())
